# Simple arithmetic operations

a = 10
b = 5

# Addition
add = a + b
print("Addition:", add)

# Subtraction
sub = a - b
print("Subtraction:", sub)

# Multiplication
mul = a * b
print("Multiplication:", mul)

# Division
div = a / b
print("Division:", div)

# Modulus
mod = a % b
print("Modulus:", mod)
